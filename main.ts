function levelUp () {
    if (level == 1) {
        tiles.setCurrentTilemap(tilemap`level1`)
    }
    if (level == 2) {
        tiles.setCurrentTilemap(tilemap`level2`)
    }
    if (level == 3) {
        tiles.setCurrentTilemap(tilemap`level3`)
    }
}
